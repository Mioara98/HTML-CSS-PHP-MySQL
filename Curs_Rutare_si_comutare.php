</p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 1</i></b> Înțelegerea rețelelor și a elementelor de bază ale acestora</span></p>
	</li>
</ul>
	<p style="text-align: justify;">Principalele elemente de bază ale rețelelor sunt stațiile de lucru, aplicațiile de rețea, tipul de rețea, serverele și routerele. Trebuie să existe stații de lucru pentru ca o rețea să fie prezentă.</p>

<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 2</i></b> Adresarea IP și subrețelele</span></p>
	</li>
</ul>
	<p style="text-align: justify;">O adresă IP este o adresă utilizată pentru a identifica în mod unic un dispozitiv dintr-o rețea IP. Adresa este formată din 32 de biți binari, care pot fi divizați într-o porțiune de rețea și o porțiune de gazdă cu ajutorul unei măști de subrețea. Valoarea fiecărui octet variază de la 0 la 255 zecimale sau 00000000 - 11111111 binar. </p>

<ul style="margin-left: 40px;">
	<li>
<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 3</i></b> Introducere în routere, comutatoare și IOS</span></p>
	</li>
</ul>
	<p style="text-align: justify;">Cisco Internetwork Operating System (IOS) este o familie de sisteme de operare în rețea utilizate pe multe routere Cisco Systems și switch-uri de rețea Cisco actuale.</p>

<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 4</i></b> Introducere în rutarea IP</span></p>
	</li>
</ul>
	<p style="text-align: justify;">Rutarea IP descrie procesul de determinare a căii de urmat a datelor pentru a naviga de la un computer sau server la altul. Un pachet de date trece de la routerul sursă printr-o rețea de routere prin mai multe rețele până când ajunge în cele din urmă la routerul de destinație folosind un algoritm de rutare. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 5</i></b> Protocoale de rutare</span></p>
	</li>
</ul>
	<p style="text-align: justify;">Un protocol de rutare este utilizat pentru a livra traficul aplicației. Oferă informații de adresare adecvate în stratul său de internet sau stratul de rețea pentru a permite redirecționarea unui pachet dintr-o rețea în alta. Exemple de protocoale rutate sunt Protocolul Internet (IP) și Internetwork Packet Exchange (IPX). </p>
	<p style="text-align: justify;"></p>

<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 6</i></b> Protocolul de comutare și spanning tree</span></p>
	</li>
</ul>
	<p style="text-align: justify;">Spanning Tree Protocol (STP) este un protocol Layer 2 care rulează pe comutatoare. Specificația pentru STP este IEEE 802.1D. Scopul principal al STP este de a vă asigura că nu creați bucle atunci când aveți căi redundante în rețea. Buclele sunt mortale pentru o rețea.</p>

<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 7</i></b> VLAN-uri și VTP</span></p>
	</li>
</ul>
		<p style="text-align: justify;">Rețeaua locală virtuală (VLAN) Trunk Protocol (VTP) reduce administrarea într-o rețea comutată. Când configurați un nou VLAN pe un singur server VTP, VLAN este distribuit prin toate comutatoarele din domeniu. VTP se asigură că toate comutatoarele din domeniul VTP sunt conștiente de toate VLAN-urile. </p>

