	<li>
	<p style="text-align: justify;"><span style="font-size:18px;">Cum funcționează Cyber ​​Essential?</span></p>
	</li>
				</ul>
	<p style="text-align: justify;">Majoritatea hackerilor infracționali nu reprezintă agenții sponsorizate de stat sau activiști care caută ținte de profil înalt și nu petrec nenumărate ore să se ocupe și să își cerceteze obiectivele.
În schimb, tind să fie oportuniști, în căutarea oricărei ținte disponibile. În această privință, vă puteți gândi la ei ca la niște spărgători; da, sunt conștienți de țintele de valoare ridicată, dar este mai eficient să urmărească ținte mai ușoare.
Și, așa cum un spărgător identifică acele urme prin cercetarea cartierelor și căutarea unor case slab protejate, la fel și infractorii cibernetici caută slăbiciuni ușor de exploatat, mai degrabă decât să identifice o anumită țintă și să încerce să găsească o cale de acces.
Cyber ​​Essentials abordează acest lucru, ajutând organizațiile să evite punctele slabe și să remedieze vulnerabilitățile înainte ca hackerii âsă aibă șansa de a le exploata.
Schema conține cinci controale, fiecare concentrându-se pe un aspect specific al securității informațiilor. Odată aplicate, vă pot proteja de 80% dintre cele mai frecvente atacuri cibernetice.</p><br>
	<p style="margin-left: 40px; text-align: justify;"><span style="font-size:14px;"><span style="background:white"><span arial="" lang="RO"><h2><b>Iată ce vei învăța în fiecare capitol:</b></h2></span></span></span></p>
				<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 1</i></b>- Firewall-uri</span></p>
	</li>
				</ul>
<p style="text-align: justify;">- Acestea sunt concepute pentru a preveni accesul neautorizat la sau din rețelele private, dar o configurare bună a acestor dispozitive fie în hardware, fie în software este esențială pentru ca acestea să fie pe deplin eficiente. </p>
<p style="text-align: justify;">- Firewall-urile limită și gateway-urile de Internet determină cine are permisiunea de a accesa sistemul dvs. de pe Internet și vă permite să controlați unde pot merge utilizatorii dvs. </p>
<p style="text-align: justify;">- Deși software-ul antivirus ajută la protejarea sistemului împotriva virușilor și a malware-ului, un firewall ajută la prevenirea accesului la sistemul dvs. de către atacatori sau amenințări externe. </p>
<p style="text-align: justify;">- Securitatea oferită de firewall poate fi ajustată ca orice altă funcție de control (cu alte cuvinte, „regulile” firewall-ului). </p>
				<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 2</i></b>- Configurare securizată</span></p>
	</li>
				</ul>
<p style="text-align: justify;">- Configurările serverului web și ale serverelor de aplicații joacă un rol crucial în securitatea cibernetică. Eșecul de a gestiona configurarea corectă a serverelor dvs. poate duce la o mare varietate de probleme de securitate. </p>
<p style="text-align: justify;">- Calculatoarele și dispozitivele de rețea ar trebui să fie configurate pentru a minimiza numărul de vulnerabilități inerente și pentru a furniza numai serviciile necesare pentru îndeplinirea funcției lor intenționate. </p>
<p style="text-align: justify;">- Acest lucru va ajuta la prevenirea acțiunilor neautorizate și va asigura, de asemenea, că fiecare dispozitiv dezvăluie doar informațiile minime despre sine către Internet. O scanare poate dezvălui oportunități de exploatare printr-o configurație nesigură. </p>
				<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 3</i></b> -Controlul de acces al utilizatorului</span></p>
	</li>
				</ul>
<p style="text-align: justify;">- Este important să mențineți accesul la datele și serviciile dvs. la un nivel minim. Acest lucru ar trebui să împiedice un hacker criminal să aibă acces deschis la informațiile dvs. </p>
<p style="text-align: justify;">- Obținerea drepturilor de administrator este un obiectiv cheie pentru hackeri criminali, permițându-le să obțină acces neautorizat la aplicații și alte date sensibile. Confortul duce uneori la faptul că mulți utilizatori au drepturi de administrator, ceea ce poate crea oportunități de exploatare. </p>
<p style="text-align: justify;">- Conturile de utilizator, în special cele cu privilegii speciale de acces, ar trebui să fie atribuite numai persoanelor autorizate, gestionate eficient și să asigure nivelul minim de acces la aplicații, computere și rețele. </p>
				<ul style="margin-left: 40px;"> 
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 4</i></b>- Protecție împotriva malware-ului</span></p>
	</li>
				</ul>
<p style="text-align: justify;">- Este esențial să vă protejați afacerea de software rău intenționat, care va căuta să acceseze fișierele din sistemul dvs. </p>
<p style="text-align: justify;">- Software-ul poate face ravagii obținând acces și furând informații confidențiale, deteriorând fișierele și chiar blocându-le și împiedicând accesul, cu excepția cazului în care plătiți o răscumpărare. </p>
<p style="text-align: justify;">- Protejarea împotriva unei game largi de programe malware (inclusiv viruși de computer, viermi, spyware, software pentru botnet și ransomware) și inclusiv opțiuni pentru eliminarea virusului vă vor proteja computerul, confidențialitatea și documentele importante de atac. </p>
				<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 5</i></b> -Gestionarea patch-urilor</span></p>
	</li>
				</ul>
<p style="text-align: justify;">- Infractorii cibernetici exploatează deseori vulnerabilitățile cunoscute. Toate dispozitivele și software-urile sunt predispuse la vulnerabilități tehnice. </p>
<p style="text-align: justify;">- Infractorii cibernetici pot exploata rapid vulnerabilitățile după ce au fost descoperiți și distribuiți public. </p>
<p style="text-align: justify;">- Hackerii criminali profită de vulnerabilitățile cunoscute ale sistemelor de operare și ale aplicațiilor de la terți, dacă nu sunt corecte sau actualizate. </p>
<p style="text-align: justify;">- Actualizarea software-ului și a sistemelor de operare va ajuta la remedierea acestor puncte slabe cunoscute. Este crucial să faceți acest lucru cât mai repede posibil pentru a închide orice oportunități care ar putea fi utilizate pentru a avea acces. </p>
