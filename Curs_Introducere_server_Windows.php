<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 1</i></b> - Instalare Windows Server</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Învățați într-un mod foarte ușor și practic pașii de instalare pentru Windows Server 2012. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><b>Capitolul 2</i></b> - Unealta Server Manager</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Server Manager este o consolă de administrare în Windows Server care ajută profesioniștii IT să furnizeze și să gestioneze atât servere locale, cât și la distanță bazate pe Windows de pe desktopurile lor, fără a necesita acces fizic la servere sau necesitatea activării conexiunilor protocolului Remote Desktop (rdP) la fiecare Server. Deși Server Manager este disponibil în Windows Server 2008 R2 și Windows Server 2008, Server Manager a fost actualizat în Windows Server 2012 pentru a susține gestionarea la distanță, multi-server și pentru a ajuta la creșterea numărului de servere pe care le poate administra un administrator. </p>
	<p style="text-align: justify;">- Aflați funcțiile de bază ale uneltei Server Manager </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 3</i></b> - Power Shell</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Windows PowerShell este un shell de linie de comandă și un limbaj de criptare conceput special pentru administrarea sistemului. Windows PowerShell ajută profesioniștii IT să controleze și să automatizeze administrarea sistemului de operare Windows și a aplicațiilor care rulează pe mediul Windows Server. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 4</i></b> - Adăugarea de roluri</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Rolurile serverului se referă la rolurile pe care serverul dvs. le poate juca în rețeaua dvs. - roluri precum un server de fișiere, un server web sau un server DHCP sau DNS. Funcțiile se referă la capabilități suplimentare ale sistemului de operare Windows în sine, cum ar fi. NET Framework sau Windows Backup. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 5</i></b> - Server DHCP</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- În acest capitol vei învăța cum funcționează un server DHCP și ce roluri are. </p>
	<p style="text-align: justify;">- Un server DHCP este un server de rețea care furnizează și atribuie automat adrese IP, gateway-uri implicite și alți parametri de rețea dispozitivelor client. Se bazează pe protocolul standard cunoscut sub numele de Dynamic Host Configuration Protocol sau DHCP pentru a răspunde la interogările de difuzare ale clienților. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 6</i></b> - Configurare DC</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Un controler de domeniu este un server care răspunde la solicitările de autentificare și verifică utilizatorii din rețelele de calculatoare. Domeniile sunt un mod ierarhic de organizare a utilizatorilor și computerelor care funcționează împreună în aceeași rețea. Controlorul de domeniu păstrează toate aceste date organizate și securizate.</p>
