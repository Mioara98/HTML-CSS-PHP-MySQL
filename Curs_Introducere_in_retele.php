<p style="margin-left: 40px; text-align: justify;"><span style="font-size:14px;"><span style="background:white"><span arial="" lang="RO"><h2><b>Iată ce vei învăța în fiecare capitol:</b></h2></span></span></span></p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 1</i></b> -Ce este o rețea?</span></p>
	</li>
</ul>

<p style="text-align: justify;">- O rețea: un grup de dispozitive care pot comunica între ele prin legături. Fiecare dispozitiv este numit gazdă. Fiecare gazdă are o adresă unică. </p>
<p style="text-align: justify;">- O rețea internet: o rețea de rețele. Pe internet, fiecare gazdă are o adresă de forma n / h unde n este numărul rețelei și h este numărul gazdei din rețeaua n. Atâta timp cât toate rețelele de pe internet au numere de rețea unice, combinarea numărului de rețea cu numărul de gazdă va da nume globale unice. Prin urmare, din exterior o rețea internet arată ca o singură rețea! </p>
<p style="text-align: justify;">- Un router: un dispozitiv care apare simultan pe două sau mai multe rețele. (De obicei, acesta este un computer sau dispozitiv cu două sau mai multe plăci de interfață de rețea sau NIC-uri.) </p>
<p style="text-align: justify;">- Internetul: cel mai mare internet din jur. „O știi când o vezi.” </p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 2</i></b> -De ce avem nevoie de internet?</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- O rețea internet poate începe cu un singur ISP global, apoi vor apărea mai mulți ISP, apoi unii ISP regionali, etc. Apoi, furnizorii mari de conținut ar putea construi și rețele de livrare a conținutului. </p>
	<p style="text-align: justify;">- Rețelele de intenet ne oferă o serie de avantaje: </p>
	<p style="text-align: justify;">- Heterogenitate </p>
	<p style="text-align: justify;">- Rețelele care apar pe internet nu trebuie să aibă același tip. Este bine să conectați o rețea token-ring la una Ethernet sau un FDDI la un X.25, etc. Sotware ne oferă iluzia unei rețele unice, universale.</p>
	<p style="text-align: justify;">- Performanţă </p>
	<p style="text-align: justify;">- Segmentarea unei rețele într-un internet folosind routerele poate crește performanța în multe cazuri, deoarece unele rețele funcționează prin difuzarea cadrelor către orice alt dispozitiv din rețea. Izolarea unui segment deosebit de ocupat reduce traficul cu care trebuie să suporte restul dispozitivelor. </p>
	<p style="text-align: justify;">- Securitate </p>
	<p style="text-align: justify;">- Ruterele pot fi programate pentru a permite doar anumit trafic să treacă către anumite segmente. </p>
	<p style="text-align: justify;">- Economie</p>
	<p style="text-align: justify;">- Unele tehnologii de rețea (cum ar fi Ethernet și Wi-fi) sunt foarte rapide, dar pot funcționa doar pe distanțe scurte. Conectarea unei rețele locale la internet permite comunicarea într-o zonă mult mai largă. Tehnologiile care permit o comunicare super rapidă pe sute de kilometri sau mai mult sunt foarte scumpe, așa că nu sunt folosite în case, ci la baza internetului. </p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 3</i></b>-Cum să studiezi rețeaua computerizată</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Domeniul rețelelor de calculatoare este foarte mare și are câteva domenii de studiu suprapuse. </p>
	<p style="text-align: justify;">- O împărțire de bază a câmpului în subiecte este: </p>
	<p style="text-align: justify;">- Transmiterea datelor </p>
	<p style="text-align: justify;">- Comutare de pachete </p>
	<p style="text-align: justify;">- Arhitectura rețelei </p>
	<p style="text-align: justify;">- Aplicații de rețea</p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 4</i></b> -Principalele concepte de rețea</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Înainte de a studia fiecare subiect în detaliu, ar trebui să obținem imagini de ansamblu ale celor mai importante subiecte conceptuale care fac posibile rețelele și funcționarea prin internet. Aceste subiecte conceptuale sunt: </p>
	<p style="text-align: justify;">- Layere </p>
	<p style="text-align: justify;">- Pachete </p>
	<p style="text-align: justify;">- Rutare </p>
	<p style="text-align: justify;">- Securitate </p>
