<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 1</i></b> - Ce reprezintă administrarea unui sistem?</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Bine ați venit la cursul de introducere în administrare sistemelor informatice! În prima săptămână a acestui curs, vom aborda elementele de bază ale administrării sistemului. Vom acoperi politicile organizaționale, serviciile de infrastructură IT, furnizarea de utilizatori și hardware, întreținerea de rutină, depanarea și gestionarea problemelor potențiale. Până la sfârșitul acestui capitol, veți înțelege rolurile și responsabilitățile unui administrator de sistem. Așadar, haideți să începem! </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 2</i></b> - Servicii de rețea și infrastructură</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Tot în prima săptămână a acestui curs, vom afla despre serviciile de rețea și infrastructură. Vom afla ce sunt serviciile de infrastructură IT și care este rolul lor în administrarea sistemului. Vom afla, de asemenea, despre sistemele de operare ale serverelor, virtualizarea, serviciile de rețea, DNS pentru serviciile web și cum să depanăm serviciile de rețea. Până la sfârșitul acestui modul, veți cunoaște cele mai comune servicii de infrastructură IT pe care le veți întâlni atunci când gestionați sarcinile de administrare a sistemului. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 3</i></b> - Servicii software</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- În a doua săptămână a acestui curs, vom explora software-ul și serviciile de platformă. Vom acoperi ce tipuri de software și servicii de platformă puteți întâlni într-un rol tehnologic și cum să le gestionați. Vom învăța cum să configurăm serviciile de e-mail, servicii de securitate, servicii de fișiere, servicii de imprimare și servicii de platformă. Vom explora modalități de depanare a serviciilor platformei și a problemelor obișnuite care trebuie luate în considerare. Până la sfârșitul acestui modul, veți înțelege cum să configurați și să gestionați serviciile de infrastructură IT pentru a ajuta o companie să rămână productivă, să păstreze informațiile în siguranță și să livreze aplicații utilizatorilor săi. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 4</i></b> - Servicii de directoare</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Tot în a doua săptămână a acestui curs, vom afla despre serviciile de directoare. Mai exact, vom acoperi modul în care două dintre cele mai populare servicii de directoare, Active Directory și OpenLDAP, funcționează în acțiune. Vom explora conceptul de gestionare centralizată și modul în care acest lucru poate ajuta SysAdmins să mențină și să susțină toate diferitele părți ale unei infrastructuri IT. Până la sfârșitul acestui modul, veți ști cum să adăugați utilizatori, parole și să utilizați politicile de grup în Active Directory și OpenLDAP. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 5</i></b> - Recuperare date și copii de rezervă</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- În a treia săptămână a acestui curs, vom afla despre recuperarea datelor și copiilor de rezervă. În orice rol tehnologic, este important să știi cum să faci backup și să recuperezi date - este și mai important pentru administrarea sistemului. Vom afla, de asemenea, despre practicile obișnuite ale corporației, cum ar fi proiectarea unui plan de recuperare în caz de accident și scrierea documentației post-mortem. Până la sfârșitul acestui capitol, veți cunoaște diferențele dintre copiile de rezervă la fața locului și cele din afara site-ului, veți înțelege valoarea și importanța testelor de backup și recuperare, veți cunoaște diferite opțiuni pentru backupul datelor (precum și riscurile) și veți înțelege scopul și conținutul unui plan de recuperare în caz de dezastru. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 6</i></b> - Proiect final</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Felicitări, ai ajuns la ultima săptămână a acestui curs! Ultima săptămână a acestui curs este dedicată proiectului final. Pentru proiectul final, veți aplica toate abilitățile pe care le-ați învățat în acest curs, oferind consultări de administrare a sistemelor. Veți evalua infrastructura IT a trei companii fictive (dar foarte reale!) Și veți oferi recomandări și sfaturi despre cum să le susțineți infrastructura IT. Până la sfârșitul acestui proiect, veți demonstra abilitățile și tehnicile de rezolvare a problemelor unui administrator de sisteme informatice. Noroc! </p>
