			<span style="background:white">
				<h2><b>Iată ce vei învăța în fiecare capitol:</b>
			</h2>
		</span>
	</span>
</p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 1</i></b> - De ce avem nevoie de apărarea cibernetică?</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Se poate spune pe bună dreptate că generația de astăzi trăiește pe internet, iar noi, utilizatorii generali, suntem aproape ignoranți cu privire la modul în care acei biți aleatori de 1 și 0 ajung în siguranță la computerul nostru. Pentru un hacker, este o epocă de aur. Cu atât de multe puncte de acces, IP-uri publice și trafic constant și tone de date de exploatat, hackerii cu pălărie neagră sunt foarte fericiți că au șansa de a exploata vulnerabilitățile și de a crea software rău intenționat pentru utilizatori. Mai mult decât atât, atacurile cibernetice evoluează în fiecare zi. Hackerii devin mai inteligenți și mai creativi cu malware-ul lor și modul în care ocolesc scanările de viruși și firewall-urile încurajează încă mulți alții. </p>
	<p style="text-align: justify;">- Prin urmare, trebuie să existe un fel de protocol care să ne protejeze împotriva tuturor acestor atacuri cibernetice și să se asigure că datele noastre nu cad în mâinile greșite. Tocmai de aceea avem nevoie de securitate cibernetică. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 2</i></b> - Tipuri de atacuri cibernetice</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Programe malware. Malware-ul este un termen folosit pentru a descrie software-ul rău intenționat, inclusiv spyware, ransomware, viruși și viermi.</p>
	<p style="text-align: justify;">- Phishing. </p>
	<p style="text-align: justify;">- Atac man-in-the-middle. </p>
	<p style="text-align: justify;">- Atac de refuz de serviciu. </p>
	<p style="text-align: justify;">- Injecție SQL. </p>
	<p style="text-align: justify;">- Exploatare zero-day. </p>
	<p style="text-align: justify;">- Tunelare DNS. </p>

<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 3</i></b> - Ce este securitatea cibernetică?</span></p>
	</li>
</ul>

	<p style="text-align: justify;">- Securitatea cibernetică se referă la un set de tehnici utilizate pentru a proteja integritatea rețelelor, programelor și datelor de atac, daune sau acces neautorizat.Din punct de vedere al calculului, securitatea cuprinde securitatea cibernetică și securitatea fizică - ambele sunt utilizate de întreprinderi pentru a proteja împotriva accesului neautorizat la centrele de date și alte sisteme computerizate. Securitatea informațiilor, care este concepută pentru a menține confidențialitatea, integritatea și disponibilitatea datelor, este un subset al securității cibernetice. Utilizarea securității cibernetice poate ajuta la prevenirea atacurilor cibernetice, a încălcării datelor și a furtului de identitate și poate ajuta la gestionarea riscurilor. </p>
	<p style="text-align: justify;">- Deci, atunci când vorbim despre securitatea cibernetică, ne-am putea întreba „De ce încercăm să ne protejăm?” Ei bine, există trei aspecte principale pe care încercăm să le controlăm: </p>
	<p style="text-align: justify;">- Acces neautorizat </p>
	<p style="text-align: justify;">- Ștergere neautorizată </p>
	<p style="text-align: justify;">- Modificare neautorizată </p>
	<p style="text-align: justify;">- Acești trei termeni sunt sinonimi cu triada CIA foarte cunoscută, care înseamnă Confidențialitate, Integritate și Disponibilitate. Triada CIA este, de asemenea, denumită în mod obișnuit cei trei piloni ai securității și majoritatea politicilor de securitate ale unei organizații sunt construite pe baza acestor trei principii.</p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 4</i></b> - Triada CIA</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Triada CIA, care înseamnă Confidențialitate, Integritate și Disponibilitate, este un model de proiectare pentru a ghida companiile și organizațiile să își formeze politicile de securitate. Este, de asemenea, cunoscut sub numele de triada AIC pentru a evita confuzia cu Agenția Centrală de Informații (CIA). Componentele triadei sunt considerate a fi cele mai importante și fundamentale componente ale securității.Aceste componente sunt : </p>
	<p style="text-align: justify;">- Confidențialitate:Confidențialitatea este protecția informațiilor personale. Confidențialitatea înseamnă păstrarea informațiilor unui client între dvs. și client și să nu le spuneți altora, inclusiv colegilor de muncă, prietenilor, familiei etc. </p>
	<p style="text-align: justify;">- Integritate:Integritatea, în contextul sistemelor informatice, se referă la metodele de asigurare a faptului că datele sunt reale, exacte și protejate de modificările neautorizate ale utilizatorului. </p>
	<p style="text-align: justify;">- Disponibilitate:Disponibilitatea, în contextul unui sistem informatic, se referă la capacitatea unui utilizator de a accesa informații sau resurse într-o locație specificată și în formatul corect. </p>
<ul style="margin-left: 40px;">
	<li>
	<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 5</i></b> - Cum este implementată securitatea cibernetică?</span></p>
	</li>
</ul>
<ul style="margin-left: 40px;">
	<p style="text-align: justify;">- Există numeroase proceduri pentru implementarea efectivă a securității cibernetice, dar există trei pași principali atunci când se remediază efectiv o problemă legată de securitate. </p>
	<p style="text-align: justify;">- Primul pas este să recunoaștem problema care cauzează problema de securitate, de exemplu, trebuie să recunoaștem dacă există un atac de refuz de serviciu sau un atac în mijlocul unui om. Următorul pas este de a evalua și analiza problema. Trebuie să ne asigurăm că izolăm toate datele și informațiile care ar fi putut fi compromise în atac. În cele din urmă, după evaluarea și analiza problemei, ultimul pas este dezvoltarea unui patch care rezolvă de fapt problema și readuce organizația într-o stare de funcționare. </p>
	<p style="text-align: justify;">- Atunci când identificăm, analizăm și tratăm un atac cibernetic, există trei principii care sunt luate în considerare pentru diferite calcule. Ele sunt: </p>
	<p style="text-align: justify;">- Vulnerabilitate </p>
	<p style="text-align: justify;">- Amenințare </p>
	<p style="text-align: justify;">- Risc </p>
	 
