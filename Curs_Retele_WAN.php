	<p style="margin-left: 40px; text-align: justify;"><span style="font-size:14px;"><span style="background:white"><span arial="" lang="RO"><h2><b>Iată ce vei învăța în fiecare capitol:</b></h2></span></span></span></p>

<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 1</i></b> - Cum a luat naștere WAN ?</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Prima retea WAN cunoscută a fost creată de Forțele Aeriene ale SUA la sfârșitul anilor 1950 pentru a interconecta site-urile din sistemul de apărare radar Semi-Automatic Ground Environment (SAGE). O rețea enormă de linii telefonice, telefoane și modemuri dedicate a legat site-urile. </p>
	<p style="text-align: justify;">- Fundația internetului bazat pe IP a început cu Advanced Research Projects Agency Network (ARPANET), prima rețea de comutare de pachete cu zonă distribuită cu control distribuit și prima rețea care implementează suita de protocol TCP / IP. </p>
	<p style="text-align: justify;">- ARPANET a interconectat inițial Universitatea din California, Los Angeles (UCLA), Stanford Research Institute (acum SRI International), Universitatea din California, Santa Barbara (UCSB) și Universitatea din Utah. </p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 2</i></b> Ce este un router WAN?</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Un router WAN, cunoscut și sub numele de router de margine sau router de frontieră, este un dispozitiv care direcționează pachetele de date între locațiile WAN, oferind unei întreprinderi acces la o rețea de operator. De-a lungul timpului au fost dezvoltate mai multe protocoale WAN, inclusiv pachete peste SONET / SDH (PoS), comutare multiprotocol etichetă (MPLS), ATM și Frame Relay.</p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 3</i></b> Ce este WAN definită de software (SD-WAN)?</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- WAN definită de software (SD-WAN) este o abordare pentru a facilita implementarea, operarea și administrarea arhitecturilor WAN. Se bazează pe virtualizare, politici la nivel de aplicație și rețele de suprapunere, precum și pe dispozitive SD-WAN și platforme software la fața locului. </p>
	<p style="text-align: justify;">- SD-WAN crește eficiența transferului de date într-o rețea WAN prin mutarea traficului către legături de rețea cu costuri mai mici pentru a face lucrările liniilor închiriate sau MPLS mai scumpe. </p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 4</i></b> Ce este optimizarea WAN?</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Constrângerile de latență și lățime de bandă cauzează adesea probleme de performanță în rețelele WAN de întreprindere. Optimizarea WAN utilizează o varietate de tehnici, inclusiv deduplicarea, compresia, optimizarea protocolului, modelarea traficului și stocarea în cache locală. Aceste tehnici îmbunătățesc livrarea pachetelor și controlul traficului, permițând la rândul lor lățimea de bandă a rețelei să crească sau să se micșoreze dinamic, după cum este necesar. </p>
	<p style="text-align: justify;">- Tehnologia SD-WAN și optimizarea WAN pot fi utilizate separat sau împreună. Unii furnizori de SD-WAN adaugă caracteristici de optimizare WAN la produsele lor. </p>
<ul style="margin-left: 40px;">
	<li>
		<p style="text-align: justify;"><span style="font-size:18px;"><i><b>Capitolul 5</i></b> Tipuri de tehnologii WAN</span></p>
	</li>
</ul>
	<p style="text-align: justify;">- Comutare de pachete</p>
	<p style="text-align: justify;">- Suită de protocol TCP / IP </p>
	<p style="text-align: justify;">- Router </p>
	<p style="text-align: justify;">- Suprapunerea rețelei </p>
	<p style="text-align: justify;">- Pachet peste SONET / SDH (PoS) </p>
	<p style="text-align: justify;">- Multiprotocol Label Switching (MPLS) </p>
	<p style="text-align: justify;">- ATM </p>
	<p style="text-align: justify;">- Frame Relay </p>
